//! 业务核心逻辑 (Domain + Application)
//!
//! Domain (领域模型): 可以理解为 "干活的工具人".
//!
//! 举个例子: 你叫一个人去发短信, 他只管发, 他不会去管是谁让他发的, 也不会去管发送的结果是成功还是失败.
//!
//! Application (应用逻辑): 这个可以理解成 "安排工作的主管".
//!
//! 比如 "用户注册" 这件事, 它就会安排流程:
//!   1. 先叫 Domain 去发个短信验证码
//!   2. 然后等用户输入验证码后去验证
//!   3. 验证通过就把用户信息写进数据库
//!
//! 调用规则:
//!   - Application 可以调用多个 Domain
//!   - Domain 之间不能相互调用 (各自独立, 互不干涉)
//!   - Domain 也不调用 Application (它不关心业务流程)

